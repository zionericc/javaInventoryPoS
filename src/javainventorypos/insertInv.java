    private boolean checkDupe(){
        int dupe = 0;
        
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/inventory","root","");

                String prodcode = (String) pcode.getText();

                String sql = "SELECT * FROM transactions WHERE prodcode='"+prodcode+"'";
                stmt = con.createStatement();
                rs = stmt.executeQuery(sql);

                while (rs.next()) {
                    dupe = dupe+1;
                }
            
        }
        catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);  
        }
        
        if (dupe >= 1){ //there is dupe product
                    return true;
                }
                else{
                    return false;
                }
    }
    
    private void addTrans(){
        try{
            con = DriverManager.getConnection("jdbc:mysql://localhost/inventory","root","");

            
//                LocalDate transdate = LocalDate.now(ZoneId.of( "Asia/Shanghai" ));
                String sql = "INSERT INTO transactions(prodcode,prodname,qty,price,transdate,tinout) VALUES(?,?,?,?,?,?)";
                pst = con.prepareStatement(sql);

                String prodcode = (String) pcode.getText();
                String prodname = (String) pname.getText();
                String qty = (String) pqty.getText();
                String price = (String) pprice.getText();
                String transdate = (String) pdate.getText();
                java.sql.Date sqlDate = java.sql.Date.valueOf(transdate);
                String tinout = "IN";

                pst.setString(1,prodcode);
                pst.setString(2,prodname);
                pst.setString(3,qty);
                pst.setString(4,price);
                pst.setDate(5,sqlDate);
                pst.setString(6,tinout);
                pst.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Transaction Recorded Successfully.");
            
        }
        catch (SQLException e) {
        JOptionPane.showMessageDialog(null, e);  
        }
    }
    
