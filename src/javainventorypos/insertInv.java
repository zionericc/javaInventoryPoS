    private void sbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sbuttonActionPerformed
        // TODO add your handling code here:
        if (pcode.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please input Product Code.");
                }
        else if (pname.getText().equals("")){
                JOptionPane.showMessageDialog(null, "Please input Product Name.");
                }
        else if (isNumeric((String) pqty.getText()) == false){
                JOptionPane.showMessageDialog(null, "Quantity must be a number.");
                }
        else if (isNumeric((String) pprice.getText()) == false){
                JOptionPane.showMessageDialog(null, "Price must be a number.");
                }
        else if (isValidDate((String) pdate.getText()) == false){
                JOptionPane.showMessageDialog(null, "Date must be in YYYY-MM-DD format.");
                }
        else{
            if (checkDupe() == false){ // no dupes
                submitdb();
                addTrans();
                clearTF();
            }
            else{ //dupes
                updatedb();
                addTrans();
                clearTF();
            }
        }
    }//GEN-LAST:event_sbuttonActionPerformed

